/**
 * Промпт для извлечения зарплатных ожиданий из истории диалога
 */
export function buildSalaryExtractionPrompt(
  conversationHistory: Array<{ sender: string; content: string }>,
): string {
  const candidateMessages = conversationHistory
    .filter((msg) => msg.sender === "CANDIDATE")
    .map((msg) => msg.content)
    .join("\n");

  return `Проанализируй сообщения кандидата и извлеки зарплатные ожидания, если они упоминались.

СООБЩЕНИЯ КАНДИДАТА:
${candidateMessages}

ЗАДАЧА:
Найди упоминания зарплатных ожиданий и верни их в кратком виде.

ФОРМАТ ОТВЕТА:
- Если зарплата упоминалась: верни только цифры и валюту (например: "150000 руб", "100-120к", "от 80000")
- Если зарплата НЕ упоминалась: верни пустую строку

ВАЖНО:
- Не добавляй пояснений
- Не придумывай данные
- Верни только то, что явно сказал кандидат

ОТВЕТ:`;
}
