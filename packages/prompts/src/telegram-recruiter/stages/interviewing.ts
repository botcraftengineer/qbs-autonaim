const MessageSender = {
  CANDIDATE: "CANDIDATE",
  BOT: "BOT",
} as const;

const MessageContentType = {
  TEXT: "TEXT",
  VOICE: "VOICE",
} as const;

const VOICE_MESSAGE_FALLBACK_TEXT = "–ì–æ–ª–æ—Å–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ";

/** –ú–∞–∫—Å–∏–º—É–º –≥–æ–ª–æ—Å–æ–≤—ã—Ö –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é */
const DEFAULT_MAX_VOICE_MESSAGES = 2;

/**
 * –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ –≥–æ–ª–æ—Å–æ–≤—ã–º –æ—Ç –∫–∞–Ω–¥–∏–¥–∞—Ç–∞
 */
function isCandidateVoiceMessage(msg: {
  sender: string;
  contentType?: string;
  content: string;
}): boolean {
  if (msg.sender !== MessageSender.CANDIDATE) {
    return false;
  }

  // –ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç: –ø—Ä–æ–≤–µ—Ä—è–µ–º contentType (—Å—Ç–∞–±–∏–ª—å–Ω–æ–µ –ø–æ–ª–µ)
  if (msg.contentType === MessageContentType.VOICE) {
    return true;
  }

  // Fallback: –ø—Ä–æ–≤–µ—Ä—è–µ–º —Ç–µ–∫—Å—Ç –∫–æ–Ω—Ç–µ–Ω—Ç–∞ (–ø–æ—Å–ª–µ–¥–Ω–∏–π –≤–∞—Ä–∏–∞–Ω—Ç)
  return msg.content === VOICE_MESSAGE_FALLBACK_TEXT;
}

