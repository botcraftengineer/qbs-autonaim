function isCandidateVoiceMessage(msg: {
  sender: string;
  contentType?: string;
  content: string;
}): boolean {
  if (msg.sender !== MessageSender.CANDIDATE) {
    return false;
  }

  // –ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç: –ø—Ä–æ–≤–µ—Ä—è–µ–º contentType (—Å—Ç–∞–±–∏–ª—å–Ω–æ–µ –ø–æ–ª–µ)
  if (msg.contentType === MessageContentType.VOICE) {
    return true;
  }

  // Fallback: –ø—Ä–æ–≤–µ—Ä—è–µ–º —Ç–µ–∫—Å—Ç –∫–æ–Ω—Ç–µ–Ω—Ç–∞ (–ø–æ—Å–ª–µ–¥–Ω–∏–π –≤–∞—Ä–∏–∞–Ω—Ç)
  return msg.content === VOICE_MESSAGE_FALLBACK_TEXT;
}

