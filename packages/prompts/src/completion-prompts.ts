export interface InterviewCompletionContext {
  candidateName?: string | null;
  vacancyTitle?: string | null;
  questionCount: number;
  score?: number;
  detailedScore?: number;
  conversationHistory?: Array<{
    sender: string;
    content: string;
    contentType?: string;
  }>;
}

