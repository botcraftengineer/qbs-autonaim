- "–ë–ª–∞–≥–æ–¥–∞—Ä—é –∑–∞ –≤—Ä–µ–º—è! –°–∫–æ—Ä–æ –≤–µ—Ä–Ω—É—Å—å —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏."

