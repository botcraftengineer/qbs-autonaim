/**
 * Конфигурация группировки сообщений
 */

/** Минута в секундах */
const MINUTE = 60;

export const MESSAGE_GROUPING_CONFIG = {
  /**
   * Время ожидания после последнего сообщения перед обработкой (в секундах)
   * 
   * Если кандидат отправляет несколько сообщений подряд, система ждет
   * это время после последнего сообщения, чтобы убедиться что он закончил.
   */
  DEBOUNCE_DELAY: 2 * MINUTE,

  /**
   * Максимальное окно группировки (в секундах)
   * 
   * Максимальное время, в течение которого сообщения считаются частью одной группы.
   * Если между первым и последним сообщением прошло больше этого времени,
   * они будут обработаны отдельно.
   */
  MAX_WINDOW: 5 * MINUTE,

  /**
   * Включить группировку сообщений
   */
  ENABLE_GROUPING: true,
} as const;
